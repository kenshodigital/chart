final class ChartTest extends TestCase
{
    /**
     * @param array<string, array<string, string>> $values
     * @param array<string, array<string, string>> $expected
     *
     * @throws MathException
     */
    #[DataProvider('provideData')]
    public function testCalculate(array $values, array $expected): void
    {
        $chart  = ChartFactory::create();
        $actual = [];

        foreach ($values as $date => [
            'open'   => $open,
            'high'   => $high,
            'low'    => $low,
            'close'  => $close,
            'volume' => $volume,
        ]) {
            $result = $chart->calculate(
                new Candle(
                    BigDecimal::of($open),
                    BigDecimal::of($high),
                    BigDecimal::of($low),
                    BigDecimal::of($close),
                    BigInteger::of($volume),
                ),
            );
            $actual[$date] = [
                'DMp' => $result->DMp->toScale(4, RoundingMode::HALF_UP)->__toString(),
                'DMm' => $result->DMm->toScale(4, RoundingMode::HALF_UP)->__toString(),
                'TR'  => $result->TR->toScale(4, RoundingMode::HALF_UP)->__toString(),
            ];
        }
        $this->assertSame($expected, $actual);
    }

