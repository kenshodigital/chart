final class DIpTest extends TestCase
{
    /**
     * @param array<string, array<string, string>> $values
     * @param array<string, string>                $expected
     *
     * @throws MathException
     */
    #[DataProvider('provideData')]
    public function testCalculate(int $period, array $values, array $expected): void
    {
        $moveUp   = new MoveUp;
        $moveDown = new MoveDown;
        $DMpSMA   = new WSMA($period);
        $TR       = new TR;
        $ATR      = new WSMA($period);
        $actual   = [];

        foreach ($values as $date => [
            'high'  => $high,
            'low'   => $low,
            'close' => $close,
        ]) {
            $high      = BigDecimal::of($high);
            $low       = BigDecimal::of($low);
            $close     = BigDecimal::of($close);
            $rMoveUp   = $moveUp->calculate($high);
            $rMoveDown = $moveDown->calculate($low);
            $rDMp      = DMp::calculate($rMoveUp, $rMoveDown);
            $rDMpSMA   = $DMpSMA->calculate($rDMp);
            $rTR       = $TR->calculate($high, $low, $close);
            $rATR      = $ATR->calculate($rTR);

            if ($rDMpSMA !== null && $rATR !== null) {
                $actual[$date] = DIx::calculate(
                    $rDMpSMA,
                    $rATR,
                )->toScale(
                    4,
                    RoundingMode::HALF_UP,
                )->__toString();
            } else {
                $actual[$date] = null;
            }
        }
        $this->assertSame($expected, $actual);
    }

