final class TrendTest extends TestCase
{
    /**
     * @param array<string, array<string, string>>      $values
     * @param array<string, array<string, string|null>> $expected
     *
     * @throws MathException
     */
    #[DataProvider('provideData')]
    public function testCalculate(int $SMAPeriod, int $EMAPeriod, array $values, array $expected): void
    {
        $chart  = ChartFactory::create();
        $trend  = TrendFactory::create($SMAPeriod, $EMAPeriod);
        $actual = [];

        foreach ($values as $date => [
            'open'   => $open,
            'high'   => $high,
            'low'    => $low,
            'close'  => $close,
            'volume' => $volume,
        ]) {
            $result = $trend->calculate(
                $chart->calculate(
                    new Candle(
                        BigDecimal::of($open),
                        BigDecimal::of($high),
                        BigDecimal::of($low),
                        BigDecimal::of($close),
                        BigInteger::of($volume),
                    ),
                ),
            );
            $actual[$date] = [
                'SMA' => $result->SMA?->toScale(4, RoundingMode::HALF_UP)->__toString(),
                'EMA' => $result->EMA?->toScale(4, RoundingMode::HALF_UP)->__toString(),
                'DIp' => $result->DIp?->toScale(4, RoundingMode::HALF_UP)->__toString(),
                'DIm' => $result->DIm?->toScale(4, RoundingMode::HALF_UP)->__toString(),
                'ADX' => $result->ADX?->toScale(4, RoundingMode::HALF_UP)->__toString(),
            ];
        }
        $this->assertSame($expected, $actual);
    }

